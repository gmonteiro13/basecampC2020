#include <unistd.h>

void	ft_print_combn(int n)
{
	char	c;
	int		t;

	t = 0;
	c = 48 + t;
	while (n > 0)
	{
		while (t <= 9)
		{
			write(1, &c, 1);
			t += 1;
		}
		t = 0;
		n -= 1;
	}
}
