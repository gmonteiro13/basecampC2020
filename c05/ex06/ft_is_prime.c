int		ft_is_prime(int nb)
{
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	if (nb % 2 == 0)
	{
		if (nb == 2)
			return (1);
		else
			return (0);
	}
	else
	{
		if (nb == 9)
			return (0);
		if ((nb % 3 == 0 || nb % 5 == 0) && nb > 10)
			return (0);
		return (1);
	}
}
