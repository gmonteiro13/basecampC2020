#include <unistd.h>

void	ft_putstr(char *str);

int		main(int argc, char **str)
{
	(void)argc;
	ft_putstr(str[0]);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i += 1;
	}
	write(1, "\n", 1);
}
